$(document).ready(function(){
    $('#contact-form').submit(function(event){
        event.preventDefault();
        var formData = $(this).serialize();

        $.ajax({
            type: 'POST',
            url: 'email_script.php',
            data: formData,
            dataType: 'json',
            success: function(response){
                if(response.error){
                    // Display error messages for missing field inputs
                    for(var i=0; i<response.error.length; i++){
                        $('#error-messages').append('<p>' + response.error[i] + '</p>');
                    }
                } else {
                    // Display success message for email sent successfully
                    $('#success-message').text(response.success).show();
                    $('#error-messages').empty();
                }
            },
            error: function(){
                $('#error-messages').text('An error occurred. Please try again later.');
            }
        });
    });
});
